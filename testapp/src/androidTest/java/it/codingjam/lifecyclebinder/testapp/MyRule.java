package it.codingjam.lifecyclebinder.testapp;

import android.app.Activity;
import android.content.Context;
import android.content.pm.ActivityInfo;
import android.content.res.Configuration;
import android.support.test.InstrumentationRegistry;
import android.support.test.rule.ActivityTestRule;

import static android.support.test.espresso.Espresso.onView;
import static android.support.test.espresso.assertion.ViewAssertions.matches;
import static android.support.test.espresso.matcher.ViewMatchers.isDisplayed;
import static android.support.test.espresso.matcher.ViewMatchers.isRoot;

public class MyRule<T extends Activity> extends ActivityTestRule<T> {
    public MyRule(Class<T> activityClass) {
        super(activityClass, false, false);
    }

    @Override
    protected void beforeActivityLaunched() {
        Logger.reset();
    }

    public T launchActivity() {
        return super.launchActivity(null);
    }

    public void rotateScreen() {
        Context context = InstrumentationRegistry.getTargetContext();
        int orientation
                = context.getResources().getConfiguration().orientation;

        Activity activity = getActivity();
        activity.setRequestedOrientation(
                (orientation == Configuration.ORIENTATION_PORTRAIT) ?
                        ActivityInfo.SCREEN_ORIENTATION_LANDSCAPE :
                        ActivityInfo.SCREEN_ORIENTATION_PORTRAIT);

        onView(isRoot()).check(matches(isDisplayed()));
    }
}
